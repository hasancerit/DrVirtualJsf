package model;

public class RandevuAyrýntý {
	private String doktorIsim;
	private String hastaneIsim;
	private String bolumIsim;
	private String saat;
	private String doktorId;
	public String getDoktorIsim() {
		return doktorIsim;
	}
	public void setDoktorIsim(String doktorIsim) {
		this.doktorIsim = doktorIsim;
	}
	public String getHastaneIsim() {
		return hastaneIsim;
	}
	public void setHastaneIsim(String hastaneIsim) {
		this.hastaneIsim = hastaneIsim;
	}
	public String getBolumIsim() {
		return bolumIsim;
	}
	public void setBolumIsim(String bolumIsim) {
		this.bolumIsim = bolumIsim;
	}
	public String getSaat() {
		return saat;
	}
	public void setSaat(String saat) {
		this.saat = saat;
	}
	public String getDoktorId() {
		return doktorId;
	}
	public void setDoktorId(String doktorId) {
		this.doktorId = doktorId;
	}
	
	
}
